function readJSON(key, fallback) {
  try {
    const raw = localStorage.getItem(key);
    return raw ? JSON.parse(raw) : fallback;
  } catch (e) {
    return fallback;
  }
}

function paintKey(name, brand) {
  return String(name || "").trim().toLowerCase() + "|" + String(brand || "").trim().toLowerCase();
}
